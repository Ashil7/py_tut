import statistics as s
l1=[20,40,60,40,80,100]
print("Mean :",s.mean(l1))
print("Median :",s.median(l1))
print("Mode : ",s.mode(l1))
print("Harmonic Mean:",s.harmonic_mean(l1))
print("Statistics Variance:",s.variance(l1))
print("Statistics Median Low :",s.median_low([-12,6,6,-3,4,6,7,1,-9,22]))